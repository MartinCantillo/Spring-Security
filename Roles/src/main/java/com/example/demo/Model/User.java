package com.example.demo.Model;

import jakarta.persistence.*;
import lombok.*;

/**
 *
 * @author marti
 */
@Entity
@Data
@AllArgsConstructor
@NoArgsConstructor
public class User {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private long id;
    private String username;
    private String password;
}
